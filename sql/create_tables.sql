-- =============================================
-- 3. 学習ログテーブル（kanji_log）― 出席判定兼用
-- =============================================
-- ★ 出席基準: その日付に kanji_log のレコードが1件以上 → 出席
-- ★ 別途 attendance テーブルは使用しない
-- ★ jlpt_level / sector カラムを含む（レベル・セクター別集計用）
-- =============================================
CREATE TABLE kanji_log (
    logID      NUMBER    GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    accID      NUMBER    NOT NULL,
    kanjiID    NUMBER    NOT NULL,
    is_correct NUMBER(1) NOT NULL CHECK (is_correct IN (0, 1)),
    studied_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    jlpt_level VARCHAR2(2),
    sector     NUMBER,

    CONSTRAINT fk_log_account FOREIGN KEY (accID)
        REFERENCES account(accID) ON DELETE CASCADE,
    CONSTRAINT fk_log_kanji FOREIGN KEY (kanjiID)
        REFERENCES kanji(kanjiID) ON DELETE CASCADE
);

-- インデックス（出席確認・統計クエリ高速化用）
CREATE INDEX idx_log_acc_date ON kanji_log (accID, TRUNC(studied_at));
CREATE INDEX idx_log_acc      ON kanji_log (accID);
CREATE INDEX idx_log_level    ON kanji_log (accID, jlpt_level);
CREATE INDEX idx_log_sector   ON kanji_log (accID, jlpt_level, sector);


-- =============================================
-- 4. 出席確認クエリ（kanji_log ベース）
-- =============================================

-- [1] 本日の出席確認（accID = 1 の場合）
SELECT COUNT(*) AS today_study
FROM kanji_log
WHERE accID = 1
  AND TRUNC(studied_at) = TRUNC(SYSDATE);

-- [2] 当月の出席日一覧
SELECT DISTINCT EXTRACT(DAY FROM studied_at) AS attended_day
FROM kanji_log
WHERE accID = 1
  AND EXTRACT(YEAR  FROM studied_at) = EXTRACT(YEAR  FROM SYSDATE)
  AND EXTRACT(MONTH FROM studied_at) = EXTRACT(MONTH FROM SYSDATE)
ORDER BY attended_day;

-- [3] 当月の出席日数
SELECT COUNT(DISTINCT TRUNC(studied_at)) AS attend_days
FROM kanji_log
WHERE accID = 1
  AND EXTRACT(YEAR  FROM studied_at) = EXTRACT(YEAR  FROM SYSDATE)
  AND EXTRACT(MONTH FROM studied_at) = EXTRACT(MONTH FROM SYSDATE);



