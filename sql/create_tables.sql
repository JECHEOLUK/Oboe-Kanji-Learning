-- =============================================
-- オボエ（漢字学習Webアプリ）テーブル定義スクリプト
-- DB: Oracle Database
-- 作成日: 2026-02-22
-- =============================================
-- テーブル構成:
--   1. account   : 会員情報
--   2. kanji     : 漢字データ
--   3. kanji_log : 学習ログ（出席判定兼用）
--
-- 出席の判定方法:
--   kanji_log にその日付のレコードが1件以上 → 出席とみなす
--   ※ attendance テーブルは使用しない
-- =============================================


-- =============================================
-- 0. 既存テーブルの削除（依存関係の逆順）
-- =============================================
DROP TABLE kanji_log CASCADE CONSTRAINTS PURGE;
DROP TABLE kanji     CASCADE CONSTRAINTS PURGE;
DROP TABLE account   CASCADE CONSTRAINTS PURGE;


-- =============================================
-- 1. 会員テーブル（account）
-- =============================================
-- accID     : 内部主キー（自動採番）
-- userID    : ログインID（メールアドレス、UNIQUE）
-- userPW1   : パスワード
-- userPW2   : パスワード確認
-- email     : メールアドレス
-- phone     : 電話番号
-- nickname  : ニックネーム（UNIQUE）
-- attendance: カラムは存在するが、実際の出席はkanji_logで判定
-- regDate   : 登録日時（自動設定 / カレンダーのグレーアウト基準）
-- =============================================
CREATE TABLE account (
    accID      NUMBER        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userID     VARCHAR2(50)  NOT NULL UNIQUE,
    userPW1    VARCHAR2(100) NOT NULL,
    userPW2    VARCHAR2(100) NOT NULL,
    email      VARCHAR2(100) NOT NULL,
    phone      VARCHAR2(20),
    nickname   VARCHAR2(50)  NOT NULL UNIQUE,
    attendance VARCHAR2(50),
    regDate    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL
);


-- =============================================
-- 2. 漢字テーブル（kanji）
-- =============================================
-- kanjiindex         : 漢字識別コード（例: N5-001）
-- kanji              : 漢字1文字
-- onyomi1~3          : 音読み（最大3つ）
-- kunyomi1~3         : 訓読み（最大3つ）
-- korean_meaning     : 韓国語の意味
-- meaning_description: 意味の説明
-- example1~3         : 用例（最大3つ）
-- jlpt_level         : JLPTレベル（N5〜N1）
-- sector             : セクター番号
-- index_num          : セクター内の順番
-- created_at         : 作成日時（自動設定）
-- updated_at         : 更新日時（トリガーで自動更新）
-- =============================================
CREATE TABLE kanji (
    kanjiID             NUMBER        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kanjiindex          VARCHAR2(20)  NOT NULL UNIQUE,
    kanji               NCHAR(1)      NOT NULL,
    onyomi1             VARCHAR2(10),
    onyomi2             VARCHAR2(10),
    onyomi3             VARCHAR2(10),
    kunyomi1            VARCHAR2(30),
    kunyomi2            VARCHAR2(30),
    kunyomi3            VARCHAR2(30),
    korean_meaning      VARCHAR2(20)  NOT NULL,
    meaning_description VARCHAR2(100),
    example1            VARCHAR2(100),
    example2            VARCHAR2(100),
    example3            VARCHAR2(100),
    jlpt_level          VARCHAR2(2)   NOT NULL,
    sector              NUMBER        NOT NULL,
    index_num           NUMBER        NOT NULL,
    created_at          TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at          TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT ck_kanji_jlpt CHECK (jlpt_level IN ('N5', 'N4', 'N3', 'N2', 'N1'))
);

